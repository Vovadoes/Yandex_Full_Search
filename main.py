import sys
from io import BytesIO
import json
import requests
from PIL import Image
from Scale import scale

toponym_to_find = " ".join(sys.argv[1:])

geocoder_api_server = "http://geocode-maps.yandex.ru/1.x/"

geocoder_params = {
    "apikey": "40d1649f-0493-4b70-98ba-98533de7710b",
    "geocode": toponym_to_find,
    "format": "json"}

json_response = requests.get(geocoder_api_server, params=geocoder_params).json()

with open('my_file.json', 'w', encoding='utf-8') as file:
    json.dump(json_response, file, ensure_ascii=False, indent=2)
# Получаем первый топоним из ответа геокодера.
toponym = json_response["response"]["GeoObjectCollection"][
    "featureMember"][0]["GeoObject"]
# Координаты центра топонима:
toponym_coodrinates = toponym["Point"]["pos"]
# Долгота и широта:
toponym_longitude, toponym_lattitude = toponym_coodrinates.split(" ")

delta = scale(toponym)
# delta = '0.008'
# Собираем параметры для запроса к StaticMapsAPI:
map_params = {
    "ll": ",".join([toponym_longitude, toponym_lattitude]),
    "spn": ",".join([str(delta), str(delta)]),
    "l": "map",
    'pt': ','.join([toponym_longitude, toponym_lattitude, 'org'])

}

map_api_server = "http://static-maps.yandex.ru/1.x/"
# ... и выполняем запрос
response = requests.get(map_api_server, params=map_params)

Image.open(BytesIO(
    response.content)).show()
